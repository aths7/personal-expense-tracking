export interface BlogService {
  getAllPosts: () => Promise<{ data: BlogPost[] | null; error: Error | null }>;
  getFeaturedPosts: (limit?: number) => Promise<{ data: BlogPost[] | null; error: Error | null }>;
  getPostBySlug: (slug: string) => Promise<{ data: BlogPost | null; error: Error | null }>;
  getPostsByCategory: (category: string) => Promise<{ data: BlogPost[] | null; error: Error | null }>;
  getCategories: () => Promise<{ data: BlogCategory[] | null; error: Error | null }>;
  searchPosts: (query: string) => Promise<{ data: BlogPost[] | null; error: Error | null }>;
}

export const blogService: BlogService = {
  getAllPosts: async () => {
    try {
      // Simulate API delay
      await new Promise(resolve => setTimeout(resolve, 100));
      
      const publishedPosts = blogPosts
        .filter(post => post.is_published)
        .sort((a, b) => new Date(b.published_date).getTime() - new Date(a.published_date).getTime());
      
      return { data: publishedPosts, error: null };
    } catch (error) {
      return { data: null, error: error as Error };
    }
  },

  getFeaturedPosts: async (limit = 3) => {
    try {
      await new Promise(resolve => setTimeout(resolve, 100));
      
      const featuredPosts = blogPosts
        .filter(post => post.is_published && post.is_featured)
        .sort((a, b) => new Date(b.published_date).getTime() - new Date(a.published_date).getTime())
        .slice(0, limit);
      
      return { data: featuredPosts, error: null };
    } catch (error) {
      return { data: null, error: error as Error };
    }
  },

  getPostBySlug: async (slug: string) => {
    try {
      await new Promise(resolve => setTimeout(resolve, 100));
      
      const post = blogPosts.find(post => post.slug === slug && post.is_published);
      
      return { data: post || null, error: null };
    } catch (error) {
      return { data: null, error: error as Error };
    }
  },

  getPostsByCategory: async (category: string) => {
    try {
      await new Promise(resolve => setTimeout(resolve, 100));
      
      const categoryPosts = blogPosts
        .filter(post => post.is_published && post.category.toLowerCase() === category.toLowerCase())
        .sort((a, b) => new Date(b.published_date).getTime() - new Date(a.published_date).getTime());
      
      return { data: categoryPosts, error: null };
    } catch (error) {
      return { data: null, error: error as Error };
    }
  },

  getCategories: async () => {
    try {
      await new Promise(resolve => setTimeout(resolve, 50));
      return { data: blogCategories, error: null };
    } catch (error) {
      return { data: null, error: error as Error };
    }
  },

  searchPosts: async (query: string) => {
    try {
      await new Promise(resolve => setTimeout(resolve, 100));
      
      const searchTerm = query.toLowerCase();
      const results = blogPosts
        .filter(post => 
          post.is_published && (
            post.title.toLowerCase().includes(searchTerm) ||
            post.excerpt.toLowerCase().includes(searchTerm) ||
            post.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
            post.category.toLowerCase().includes(searchTerm)
          )
        )
        .sort((a, b) => new Date(b.published_date).getTime() - new Date(a.published_date).getTime());
      
      return { data: results, error: null };
    } catch (error) {
      return { data: null, error: error as Error };
    }
  }
};